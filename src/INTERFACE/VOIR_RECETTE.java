    private void kButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton11ActionPerformed
        // TODO add your handling code here:

        try {
            if (MENUS.grades.equals("analyste")) {
                String query = "SELECT c.nom,\n"
                        + "	r.DotationRecettes,\n"
                        + "    r.RecettesRealisees,\n"
                        + "    r.CotiseOrdinairesModeleDC,\n"
                        + "    r.CotiseComplmntairesModeleDCC,\n"
                        + "    r.MajorationRetardMO,\n"
                        + "    r.TaxationOfficeTO,\n"
                        + "    r.ImpayesCotiseRecouvresEexerCours,\n"
                        + "    r.ArrieresCotiseRecouvresExersAnt,\n"
                        + "    r.mois,\n"
                        + "    r.annee\n"
                        + "FROM recette AS r\n"
                        + "INNER JOIN centre_gestion AS c ON c.Code_centre=r.Code_centre\n"
                        + "WHERE r.annee='" + Annee.getText() + "' AND r.Matricule='" + MATRICULE.getText() + "'\n"
                        + "ORDER BY c.nom";

                JasperDesign jd = JRXmlLoader.load("src\\ETATRAPPORT\\RECETTETOUSMAT.jrxml");
                JRDesignQuery querynew = new JRDesignQuery();
                querynew.setText(query);
                jd.setQuery(querynew);
                JasperReport rapport = JasperCompileManager.compileReport(jd);
                JasperPrint jp = JasperFillManager.fillReport(rapport, null, new Connexion().con());
                JasperViewer.viewReport(jp, false);

            } else {

                JasperDesign jd = JRXmlLoader.load("src\\ETATRAPPORT\\RECEETETOUS.jrxml");
                JasperReport rapport = JasperCompileManager.compileReport(jd);
                String valeur = Annee.getText() + "%";
                Map param = new HashMap();
                param.put("ID", valeur);
                JasperPrint jp = JasperFillManager.fillReport(rapport, param, new Connexion().con());
                JasperViewer.viewReport(jp, false);
            }

        } catch (JRException ex) {
            Logger.getLogger(VOIR_RECETTE.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_kButton11ActionPerformed

